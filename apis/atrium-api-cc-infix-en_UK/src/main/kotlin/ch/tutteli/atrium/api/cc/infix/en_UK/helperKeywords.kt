/**
 * Represents the pseudo keyword `case` as in [ignoring] `case`.
 */
object case

/**
 * Represents the pseudo keyword `only` as in [but] `only`.
 */
object only

/**
 * Represents the pseudo keyword `order` as in [inAny] `order`.
 */
object order
